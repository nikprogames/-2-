private Image image;
    private Timer timer;
    private int imageX = 20;
    private int imageY = 78;
private void loadImages() {
        try {
            URL imageUrl = getClass().getResource("1685129225_polinka-top-p-girlyanda-kartinka-dlya-detei-krasivo-54.png");
            image = ImageIO.read(imageUrl);
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }
@Override
    public void paint(Graphics g) {
        super.paint(g);
        g.drawImage(image, imageX, imageY, null);
    }

