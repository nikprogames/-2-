    private void startAnimation() {
        timer = new Timer(700, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (imageX > getWidth()) {
                    imageX = -image.getWidth(
                            null);
                }
                repaint();
            }
        });
        timer.start();
    }              
